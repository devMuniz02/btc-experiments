from __future__ import annotations

import argparse
import json
import os
import re
import shutil
import subprocess
import tempfile
from pathlib import Path
from typing import Any, Callable

from src.workflows.hf_state import HfSettings, HfStateClient, LIVE_DATA_ROOT, MARKETS_ROOT, REQUEST_ROOT

MARKET_ID_PATTERN = re.compile(r"^[A-Za-z0-9_.-]+$")
BRANCH_NAME_PATTERN = re.compile(r"^[A-Za-z0-9][A-Za-z0-9._/-]*$")
PRIVATE_EXPERIMENTS_ROOT = "privateexperiments"
LOCAL_OUTPUT_ROOTS = ("experiments", "prod", "docs", PRIVATE_EXPERIMENTS_ROOT)
LOCAL_CACHE_ROOTS = (REQUEST_ROOT, MARKETS_ROOT, "models", LIVE_DATA_ROOT, "experiment_state")
HF_RESET_KEEP_ROOT_FILES = {".gitattributes", "README.md", "environment.yaml", "requirements.txt"}
DEFAULT_EXPERIMENT_BRANCH = "experiments"
DEFAULT_PROD_BRANCH = "prod"
DEFAULT_PAGES_BRANCH = "gh-pages"


def validate_market_id(market: str) -> str:
    normalized = market.strip()
    if not normalized or not MARKET_ID_PATTERN.fullmatch(normalized):
        raise ValueError("Market must be a simple id containing only letters, numbers, dot, underscore, or dash.")
    return normalized


def validate_branch_name(branch: str) -> str:
    normalized = branch.strip()
    if (
        not normalized
        or normalized.startswith("-")
        or ".." in normalized
        or normalized.endswith("/")
        or normalized.endswith(".lock")
        or not BRANCH_NAME_PATTERN.fullmatch(normalized)
    ):
        raise ValueError(f"Unsafe branch name: {branch!r}")
    return normalized


def _is_within(path: Path, parent: Path) -> bool:
    try:
        path.resolve().relative_to(parent.resolve())
        return True
    except ValueError:
        return False


def _market_child(root: Path, market: str) -> Path:
    return root / market


def local_output_paths(root: Path, *, cache_root: Path, market: str | None = None) -> list[Path]:
    resolved_cache = cache_root if cache_root.is_absolute() else root / cache_root
    if market:
        market = validate_market_id(market)
        paths = [_market_child(root / name, market) for name in LOCAL_OUTPUT_ROOTS]
        paths.extend(_market_child(resolved_cache / name, market) for name in LOCAL_CACHE_ROOTS)
        paths.append(resolved_cache / REQUEST_ROOT / "config.yaml")
        paths.append(resolved_cache / REQUEST_ROOT / "configs")
        return paths
    paths = [root / name for name in LOCAL_OUTPUT_ROOTS]
    paths.extend(resolved_cache / name for name in LOCAL_CACHE_ROOTS)
    return paths


def _allowed_local_roots(root: Path, cache_root: Path) -> list[Path]:
    resolved_cache = cache_root if cache_root.is_absolute() else root / cache_root
    return [root / name for name in LOCAL_OUTPUT_ROOTS] + [resolved_cache / name for name in LOCAL_CACHE_ROOTS]


def delete_local_outputs(root: Path, *, cache_root: Path, paths: list[Path]) -> list[str]:
    deleted: list[str] = []
    allowed = [path.resolve() for path in _allowed_local_roots(root, cache_root)]
    for path in paths:
        resolved = path.resolve()
        if not any(resolved == base or _is_within(resolved, base) for base in allowed):
            raise RuntimeError(f"Refusing to delete path outside generated output roots: {path}")
        if not resolved.exists():
            continue
        if resolved.is_dir():
            shutil.rmtree(resolved)
        else:
            resolved.unlink()
        deleted.append(str(resolved))
    return deleted


def _reset_branch_files(branch: str) -> dict[str, str]:
    if branch == DEFAULT_PAGES_BRANCH:
        return {
            ".nojekyll": "",
            "index.html": (
                "<!doctype html>\n"
                "<html lang=\"en\"><head><meta charset=\"utf-8\">"
                "<meta name=\"viewport\" content=\"width=device-width, initial-scale=1\">"
                "<title>Outputs reset</title></head>"
                "<body><main><h1>Outputs reset</h1>"
                "<p>Pages will be regenerated by the next Pages workflow run.</p>"
                "</main></body></html>\n"
            ),
        }
    return {
        "README.md": (
            f"# {branch} outputs reset\n\n"
            "This branch is reserved for generated workflow artifacts. "
            "The next workflow run will repopulate it.\n"
        )
    }


def _clear_directory_contents(path: Path) -> None:
    for child in path.iterdir():
        if child.name == ".git":
            continue
        if child.is_dir():
            shutil.rmtree(child)
        else:
            child.unlink()


def _git_stdout(root: Path, command: list[str], *, check: bool = True) -> str:
    result = subprocess.run(command, cwd=root, check=check, capture_output=True, text=True)
    return result.stdout.strip()


def _copy_git_identity(source_root: Path, target_root: Path) -> None:
    name = _git_stdout(source_root, ["git", "config", "--get", "user.name"], check=False)
    email = _git_stdout(source_root, ["git", "config", "--get", "user.email"], check=False)
    subprocess.run(["git", "config", "user.name", name or "github-actions[bot]"], cwd=target_root, check=True)
    subprocess.run(
        ["git", "config", "user.email", email or "41898282+github-actions[bot]@users.noreply.github.com"],
        cwd=target_root,
        check=True,
    )


def reset_remote_branch(root: Path, *, branch: str, dry_run: bool = False) -> dict[str, Any]:
    branch = validate_branch_name(branch)
    files = _reset_branch_files(branch)
    if dry_run:
        return {"branch": branch, "files": sorted(files), "status": "planned"}

    tmp_parent = root / "tmp"
    tmp_parent.mkdir(parents=True, exist_ok=True)
    with tempfile.TemporaryDirectory(prefix=f"reset-{branch.replace('/', '-')}-", dir=tmp_parent) as tmp:
        reset_repo = Path(tmp) / "repo"
        remote_url = _git_stdout(root, ["git", "remote", "get-url", "origin"])
        subprocess.run(["git", "init", str(reset_repo)], cwd=root, check=True)
        subprocess.run(["git", "remote", "add", "origin", remote_url], cwd=reset_repo, check=True)
        _copy_git_identity(root, reset_repo)
        reset_branch = f"reset-{os.getpid()}-{branch.replace('/', '-')}"
        subprocess.run(["git", "checkout", "--orphan", reset_branch], cwd=reset_repo, check=True)
        for relative, text in files.items():
            target = reset_repo / relative
            target.parent.mkdir(parents=True, exist_ok=True)
            target.write_text(text, encoding="utf-8")
        subprocess.run(["git", "add", "-A"], cwd=reset_repo, check=True)
        subprocess.run(["git", "commit", "-m", f"Reset {branch} outputs"], cwd=reset_repo, check=True)
        fetch = subprocess.run(["git", "fetch", "origin", branch], cwd=reset_repo)
        push = ["git", "push", "--force", "origin", f"HEAD:{branch}"]
        if fetch.returncode == 0:
            push = ["git", "push", "--force-with-lease", "origin", f"HEAD:{branch}"]
        subprocess.run(push, cwd=reset_repo, check=True)
    return {"branch": branch, "files": sorted(files), "status": "reset"}


def reset_artifact_branches(
    root: Path,
    *,
    branches: list[str],
    dry_run: bool,
    reset_branch_func: Callable[..., dict[str, Any]] = reset_remote_branch,
) -> list[dict[str, Any]]:
    return [reset_branch_func(root, branch=branch, dry_run=dry_run) for branch in branches]


def hf_output_paths(client: Any, *, market: str | None = None) -> list[str]:
    normalized_market = validate_market_id(market) if market else None
    paths: list[str] = []
    for hf_path in client.list_files(""):
        normalized = hf_path.strip("/")
        if not normalized:
            continue
        if normalized_market is None:
            if not _is_expected_hf_reset_file(normalized):
                paths.append(normalized)
            continue
        if normalized == f"{REQUEST_ROOT}/config.yaml":
            paths.append(normalized)
        elif normalized.startswith(f"{REQUEST_ROOT}/configs/"):
            paths.append(normalized)
        elif normalized.startswith(f"{REQUEST_ROOT}/phase") and normalized_market in Path(normalized).name:
            paths.append(normalized)
        elif normalized == f"{MARKETS_ROOT}/{normalized_market}.yaml":
            paths.append(normalized)
        elif normalized.startswith(f"{MARKETS_ROOT}/{normalized_market}/"):
            paths.append(normalized)
        elif normalized.startswith(f"models/{normalized_market}/"):
            paths.append(normalized)
        elif normalized.startswith(f"{LIVE_DATA_ROOT}/{normalized_market}/"):
            paths.append(normalized)
        elif normalized.startswith(f"experiment_state/{normalized_market}/"):
            paths.append(normalized)
        elif normalized.startswith(f"{PRIVATE_EXPERIMENTS_ROOT}/{normalized_market}/"):
            paths.append(normalized)
    return sorted(set(paths))


def _is_expected_hf_reset_file(normalized: str) -> bool:
    if normalized in HF_RESET_KEEP_ROOT_FILES:
        return True
    if normalized.startswith("src/"):
        return True
    if normalized.startswith(f"{REQUEST_ROOT}/configs/"):
        return True
    if normalized.startswith(f"{MARKETS_ROOT}/"):
        relative = normalized.removeprefix(f"{MARKETS_ROOT}/")
        return "/" not in relative and relative.endswith((".yaml", ".yml"))
    return False


def delete_hf_outputs(client: Any, *, paths: list[str], market: str | None = None) -> list[str]:
    label = market or "all generated outputs"
    if not paths:
        return []
    if hasattr(client, "delete_files"):
        client.delete_files(paths, commit_message=f"Reset {label}: delete generated outputs")
        return list(paths)
    deleted: list[str] = []
    for hf_path in paths:
        client.delete_file(hf_path, commit_message=f"Reset {label}: delete {hf_path}")
        deleted.append(hf_path)
    return deleted


def reset_all_outputs(
    *,
    root: Path,
    cache_root: Path,
    client: Any | None,
    dry_run: bool,
    force: bool,
    market: str | None = None,
    local_only: bool = False,
    hf_only: bool = False,
    reset_branches: bool = True,
    delete_local: bool = False,
    experiment_branch: str = DEFAULT_EXPERIMENT_BRANCH,
    prod_branch: str = DEFAULT_PROD_BRANCH,
    pages_branch: str = DEFAULT_PAGES_BRANCH,
    reset_branch_func: Callable[..., dict[str, Any]] = reset_remote_branch,
) -> dict[str, Any]:
    if force == dry_run:
        raise ValueError("Choose exactly one of --dry-run or --force.")
    if local_only and hf_only:
        raise ValueError("--local-only and --hf-only cannot be used together.")
    if market:
        market = validate_market_id(market)
    experiment_branch = validate_branch_name(experiment_branch)
    prod_branch = validate_branch_name(prod_branch)
    pages_branch = validate_branch_name(pages_branch)

    local_paths = local_output_paths(root, cache_root=cache_root, market=market) if delete_local and not hf_only else []
    branch_names = [experiment_branch, prod_branch, pages_branch] if reset_branches and market is None and not hf_only else []
    hf_paths: list[str] = []
    if not local_only:
        if client is None:
            raise RuntimeError("HF credentials are required unless --local-only is set.")
        hf_paths = hf_output_paths(client, market=market)

    result: dict[str, Any] = {
        "market": market or "all",
        "dry_run": dry_run,
        "force": force,
        "local_paths": [str(path) for path in local_paths],
        "hf_paths": hf_paths,
        "branch_resets": [],
        "branch_reset_skipped": bool(reset_branches and market is not None),
        "deleted_local_paths": [],
        "deleted_hf_paths": [],
    }
    if dry_run:
        result["branch_resets"] = reset_artifact_branches(
            root,
            branches=branch_names,
            dry_run=True,
            reset_branch_func=reset_branch_func,
        )
        return result

    if branch_names:
        result["branch_resets"] = reset_artifact_branches(
            root,
            branches=branch_names,
            dry_run=False,
            reset_branch_func=reset_branch_func,
        )
    if delete_local and not hf_only:
        result["deleted_local_paths"] = delete_local_outputs(root, cache_root=cache_root, paths=local_paths)
    if not local_only and client is not None:
        result["deleted_hf_paths"] = delete_hf_outputs(client, paths=hf_paths, market=market)
    return result


def parse_args() -> argparse.Namespace:
    parser = argparse.ArgumentParser(
        description="Reset generated artifact branches and HF workflow state from previous runs without touching main."
    )
    parser.add_argument("--root", default=".")
    parser.add_argument("--cache-root", default="private_hf_cache")
    parser.add_argument("--market", default="", help="Optional market id. Omit to reset all generated outputs.")
    parser.add_argument("--repo-id", default="")
    parser.add_argument("--token", default="")
    parser.add_argument(
        "--repo-type", default=os.environ.get("HF_REPO_TYPE", "model"), choices=["model", "dataset", "space"]
    )
    parser.add_argument("--dry-run", action="store_true")
    parser.add_argument("--force", action="store_true")
    parser.add_argument("--local-only", action="store_true")
    parser.add_argument("--hf-only", action="store_true")
    parser.add_argument("--experiment-branch", default=os.environ.get("EXPERIMENT_ARTIFACT_BRANCH", DEFAULT_EXPERIMENT_BRANCH))
    parser.add_argument("--prod-branch", default=os.environ.get("PROD_ARTIFACT_BRANCH", DEFAULT_PROD_BRANCH))
    parser.add_argument("--pages-branch", default=os.environ.get("PAGES_BRANCH", DEFAULT_PAGES_BRANCH))
    parser.add_argument("--no-reset-branches", dest="reset_branches", action="store_false")
    parser.add_argument(
        "--delete-local",
        action="store_true",
        help="Also delete local generated output/cache folders in the current checkout. Disabled by default.",
    )
    return parser.parse_args()


def main() -> int:
    args = parse_args()
    client = None
    if not args.local_only:
        client = HfStateClient(
            HfSettings.from_env_or_args(repo_id=str(args.repo_id), token=str(args.token), repo_type=str(args.repo_type))
        )
    result = reset_all_outputs(
        root=Path(args.root).resolve(),
        cache_root=Path(args.cache_root),
        client=client,
        dry_run=bool(args.dry_run),
        force=bool(args.force),
        market=str(args.market).strip() or None,
        local_only=bool(args.local_only),
        hf_only=bool(args.hf_only),
        reset_branches=bool(args.reset_branches),
        delete_local=bool(args.delete_local),
        experiment_branch=str(args.experiment_branch),
        prod_branch=str(args.prod_branch),
        pages_branch=str(args.pages_branch),
    )
    print(json.dumps(result, indent=2, sort_keys=True))
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
